class Utils {
  String getWeek(int week) {
    if (week == 1) {
      return "Mon";
    } else if (week == 2) {
      return "Tue";
    } else if (week == 3) {
      return "Wed";
    } else if (week == 4) {
      return "Thu";
    } else if (week == 5) {
      return "Fri";
    } else if (week == 6) {
      return "Sat";
    } else {
      return "Sun";
    }
  }
}
