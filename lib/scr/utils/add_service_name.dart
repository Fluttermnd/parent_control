class AddServiceName {
  String getServiceName(int serviceName) {
    if (serviceName == 1) {
      return "Instagram";
    } else if (serviceName == 2) {
      return "TikTok";
    } else if (serviceName == 3) {
      return "YouTube";
    } else if (serviceName == 4) {
      return "Twitter";
    } else if (serviceName == 5) {
      return "Steam";
    } else {
      return "Facebook";
    }
  }
}
